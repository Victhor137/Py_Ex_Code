# Condição
resposta = "S"

#bitree
while resposta == "S":
    nota1 =  float(input("Digite a nota da 1°Av: "))
    nota2 =  float(input("Digite a nota da 2°Av: "))
    #calc
    media = (nota1 + nota2)/2    
    print("A média final é {:.2f}".format(media))
    #condição de retorno
    reposta = str(input("Digite (S) para continuar e (N) para encerrar a sessão.")).upper()
    if reposta == "S":
        print("Reiniciando calculadora:")
    else:
        print("Fim da operação.")
        break
